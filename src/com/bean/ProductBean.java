package com.bean;

public class ProductBean {

	private int pId;
	private String pName;
	private int pPrice;
	private int pDisc;
	private int pQty;
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public int getpPrice() {
		return pPrice;
	}
	public void setpPrice(int pPrice) {
		this.pPrice = pPrice;
	}
	public int getpDisc() {
		return pDisc;
	}
	public void setpDisc(int pDisc) {
		this.pDisc = pDisc;
	}
	public int getpQty() {
		return pQty;
	}
	public void setpQty(int pQty) {
		this.pQty = pQty;
	}
	
	
	
	
}
