package com.bean;

public class CourseBean {

	private int cId;
	private String cName;
	private int cDur;
	private int cPrice;
	public int getcId() {
		return cId;
	}
	public void setcId(int cId) {
		this.cId = cId;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public int getcDur() {
		return cDur;
	}
	public void setcDur(int cDur) {
		this.cDur = cDur;
	}
	public int getcPrice() {
		return cPrice;
	}
	public void setcPrice(int cPrice) {
		this.cPrice = cPrice;
	}
	
	
	
}
